----------------------------------------------------------------------------
-- add_SalesStat
----------------------------------------------------------------------------
IF EXISTS (SELECT * FROM sys.objects WHERE type = 'P' AND name = 'add_SalesStat')
	DROP PROCEDURE add_SalesStat
GO

CREATE PROCEDURE add_SalesStat
	@version varchar(8), 
	@year smallint,
	@month tinyint,
	@ranking tinyint,
	@datePart varchar(2) = 'm'

AS
BEGIN
	SET NOCOUNT ON;
	
	DECLARE @versionId int = (SELECT Id FROM Versions WHERE Code = @version)
	
	IF NOT EXISTS (SELECT * FROM SalesStats WHERE VersionId = @versionId AND Year = @year AND Month = @month)
		INSERT INTO SalesStats(VersionId, Year, Month, Ranking, DatePartCode) VALUES (@versionId, @year, @month, @ranking, @datepart)

END
GO


