-- SalesStats Table
----------------------------------------------------------------------------
IF NOT EXISTS (SELECT * FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME = 'SalesStats')
CREATE TABLE SalesStats(
	Id int IDENTITY(1,1) NOT NULL,
	VersionId int NOT NULL,
	Year smallint NOT NULL,
	Month int NOT NULL,
	Ranking tinyint NOT NULL,
	DatePartCode varchar(2) NOT NULL CONSTRAINT DF_SalesStats_DatePartCode DEFAULT ('m'),
	CONSTRAINT PK_SalesStats PRIMARY KEY CLUSTERED (Id),
	CONSTRAINT FK_SalesStats_Versions FOREIGN KEY (VersionId) REFERENCES Versions (Id)
)

